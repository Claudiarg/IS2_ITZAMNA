package Controlador;

/**
 *
 * @author beth
 */
public class Uso {

    private int idUso;
    private String descripcion;

    public Uso() {
    }

    public Uso(int idUso, String descripcion) {
        this.idUso = idUso;
        this.descripcion = descripcion;
    }

    public int getIdUso() {
        return idUso;
    }

    public void setIdUso(int idUso) {
        this.idUso = idUso;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }
    
    
}
